#pragma warning(disable : 4996)
#include <cstdio>
#include <cstring>

int main() {
	int t; scanf("%d", &t);
	int u, v;
	while (t--) {
		int n, m1, m2; scanf("%d %d %d", &n, &m1, &m2);
		for (int i = 0; i < m1; i++) {
			scanf("%d %d", &u, &v);
		}
		for (int i = 0; i < m2; i++) {
			scanf("%d %d", &u, &v);
		}

	}
	return 0;
}